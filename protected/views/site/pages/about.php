<h1>Acerca de SMST</h1>

<p>SMST fue desarrollado para la Subdirección de Transporte Municipal de Mérida.</p>

<p>La Subdirección de Transporte Municipal de Mérida,
    ha aprobado el proyecto: Sistema Municipal de Solicitud de Taxis (SMST),<br>
    Dicho sistema proveerá, principalmente, administración de la información de los taxistas inscritos al Servicio Municipal de Taxis,<br>
    administración de la información de los dispositivos que los taxistas usarán para recibir solicitudes,<br>
    así como la administración de las solicitudes realizadas por los clientes.  </p>
<p>
    SMST tiene como objetivo proveer a los clientes de taxis una forma sencilla y eficiente de conseguir un taxi dentro de la ciudad de Mérida,<br>
    así como disminuir el uso de los taxis piratas. 
</p>